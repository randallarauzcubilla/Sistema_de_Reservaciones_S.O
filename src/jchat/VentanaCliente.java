    private void enviarMensaje() {
    String texto = txtIngreso.getText().trim();
    if (texto.isEmpty()) return;
    txtIngreso.setText("");
    try { salida.writeUTF(texto); salida.flush(); }
    catch (java.io.IOException e) { agregarChat("Error: " + e.getMessage()); }
    }

    private void pedirCarton() {
        try { salida.writeUTF("PEDIR_CARTON"); salida.flush(); }
        catch (java.io.IOException e) { agregarChat("Error al pedir cartón: " + e.getMessage()); }
    }

    private void declararBingo() {
        if (!cartonCargado) return;
        try { salida.writeUTF("BINGO:" + miNombre); salida.flush(); }
        catch (java.io.IOException e) { agregarChat("Error: " + e.getMessage()); }
    }
