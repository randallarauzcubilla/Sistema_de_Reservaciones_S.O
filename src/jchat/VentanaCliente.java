    private void escucharServidor() {
    while (true) {
        try {
            String msg = entrada.readUTF();
            if (msg.startsWith("CARTON:")) {
                parsearYMostrarCarton(msg.substring(7));
            } else if (msg.startsWith("NUMERO:")) {
                int n = Integer.parseInt(msg.substring(7).trim());
                procesarNumeroCantado(n);
            } else if (msg.startsWith("GANADOR:")) {
                mostrarGanador(msg.substring(8).trim());
            } else if (msg.startsWith("MSG:")) {
                agregarChat(msg.substring(4));
            } else {
                agregarChat(msg);
            }
        } catch (java.io.IOException e) {
            agregarChat("Conexión perdida."); break;
        }
    }
}

    private void parsearYMostrarCarton(String datos) {
        String[] filas = datos.split("\\|");
        if (filas.length != 5) return;
        miCarton = new int[5][5];
        Object[][] valores = new Object[5][5];
        for (int i = 0; i < 5; i++) {
            String[] cols = filas[i].split(",");
            for (int j = 0; j < 5; j++) {
                int num = Integer.parseInt(cols[j].trim());
                miCarton[i][j] = num;
                valores[i][j] = (num == 0) ? "★" : String.valueOf(num);
            }
        }
        javax.swing.SwingUtilities.invokeLater(() -> {
            tablero.setModel(new javax.swing.table.DefaultTableModel(
                valores, new String[]{"B","I","N","G","O"}) {
                @Override public boolean isCellEditable(int r, int c) { return false; }
            });
            aplicarRenderer();
            cartonCargado = true;
            agregarChat("¡Cartón recibido del servidor!");
        });
    }

    private void procesarNumeroCantado(int numero) {
        numerosRecibidos.add(numero);
        javax.swing.SwingUtilities.invokeLater(() -> {
            tablero.repaint();
            agregarChat("Número: " + letraDeNumero(numero) + "-" + numero);
        });
    }

    private void mostrarGanador(String ganador) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            boolean soiYo = ganador.equals(miNombre);
            javax.swing.JOptionPane.showMessageDialog(this,
                soiYo ? "¡GANASTE!" : "Ganó: " + ganador,
                "BINGO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            agregarChat("GANADOR: " + ganador);
        });
    }

    private void aplicarRenderer() {
        javax.swing.table.DefaultTableCellRenderer r = new javax.swing.table.DefaultTableCellRenderer() {
            @Override
            public java.awt.Component getTableCellRendererComponent(javax.swing.JTable t,
                    Object val, boolean sel, boolean foc, int row, int col) {
                super.getTableCellRendererComponent(t, val, sel, foc, row, col);
                setHorizontalAlignment(javax.swing.SwingConstants.CENTER);
                if (val == null) return this;
                if ("★".equals(val.toString())) { setBackground(COLOR_LIBRE); setForeground(java.awt.Color.WHITE); return this; }
                try {
                    int num = Integer.parseInt(val.toString());
                    boolean esUlt = !numerosRecibidos.isEmpty() &&
                        numerosRecibidos.stream().reduce((a,b)->b).orElse(-1) == num;
                    if (esUlt)                           { setBackground(COLOR_ULTIMO);  setForeground(java.awt.Color.WHITE); }
                    else if (numerosRecibidos.contains(num)) { setBackground(COLOR_MARCADO); setForeground(java.awt.Color.WHITE); }
                    else                                 { setBackground(COLOR_NORMAL);  setForeground(new java.awt.Color(52,73,94)); }
                } catch (NumberFormatException ignored) {}
                return this;
            }
        };
        for (int c = 0; c < 5; c++) tablero.getColumnModel().getColumn(c).setCellRenderer(r);
    }

    private void agregarChat(String msg) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            jtextAreaMensajes.append(msg + "\n");
            jtextAreaMensajes.setCaretPosition(jtextAreaMensajes.getDocument().getLength());
        });
    }

    private String letraDeNumero(int n) {
        if (n <= 15) return "B"; if (n <= 30) return "I";
        if (n <= 45) return "N"; if (n <= 60) return "G"; return "O";
    }

