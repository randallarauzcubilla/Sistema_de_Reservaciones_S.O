    public static void main(String args[]) {
    java.awt.EventQueue.invokeLater(() -> new VentanaCliente().setVisible(true));

    String nombre = javax.swing.JOptionPane.showInputDialog("Ingrese su nombre:");
    if (nombre == null || nombre.trim().isEmpty()) System.exit(0);

    VentanaCliente vc = null;
    // Esperar que la ventana esté lista
    try { Thread.sleep(500); } catch (InterruptedException ignored) {}

    try {
        java.net.Socket sfd = new java.net.Socket("localhost", 8000);
        java.io.DataInputStream ent = new java.io.DataInputStream(
            new java.io.BufferedInputStream(sfd.getInputStream()));
        java.io.DataOutputStream sal = new java.io.DataOutputStream(
            new java.io.BufferedOutputStream(sfd.getOutputStream()));

        sal.writeUTF(nombre.trim());
        sal.flush();

        // Guardar en la instancia
        for (java.awt.Window w : java.awt.Window.getWindows()) {
            if (w instanceof VentanaCliente) {
                vc = (VentanaCliente) w;
                vc.socket  = sfd;
                vc.entrada = ent;
                vc.salida  = sal;
                vc.miNombre = nombre.trim();
                break;
            }
        }

        if (vc == null) { System.out.println("Error: ventana no encontrada"); System.exit(1); }

        final VentanaCliente ventana = vc;
        new Thread(() -> ventana.escucharServidor(), "Listener").start();

    } catch (java.io.IOException e) {
        javax.swing.JOptionPane.showMessageDialog(null, "No se pudo conectar: " + e.getMessage());
        System.exit(1);
    }
}
    
